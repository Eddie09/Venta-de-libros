<center><p>MISIÓN:</p></center>
<p>Ofrecer a la comunidad académica un espacio, físico y virtual en donde el acceso a recursos y servicios de información y la guía de profesionales de la información posibiliten interacción,  intercambio de ideas,  desarrollo de destrezas, descubrimiento de nuevas fuentes y creación de nuevo conocimiento resultando en una experiencia empoderadora de aprendizaje de por vida.</p>
 Actualmente se trabaja con las siguientes áreas estratégicas : Biblioteca Virtual y Mercadeo  de Servicios y Productos.  Los objetivos para cumplir estas metas cubren las siguientes categorías:

<p>Avalúo</p>
 <p>Recursos Externos</p>
 <p>Iniciativas Tecnológicas</p>
<p>Capacitación y Desarrollo</p>
<p>Comunicación</p>
<center><p>Biblioteca Virtual</p></center>
<p>Convertir la Biblioteca en una entidad primordialmente virtual, mediante la adquisición de colecciones que serán en su mayoría electrónicas, las cuales serán accesibles en modalidad remota, mediante la implementación de unos servicios y procedimientos que estarán disponibles en línea, sustentados por un personal capacitado y una infraestructura confiable en términos tecnológicos.</p>
<center><p>Objetivos</p></center>
<p>Promover una cultura académica que use los medios electrónicos para enseñar, aprender, investigar, comunicar y trabajar.</p>
<p>Establecer/codificar manuales de procesos/ procedimientos/servicios y atemperar cualquiera otros documentos de la biblioteca y de la Universidad, para que reflejen el concepto de Biblioteca Virtual.</p>
<p>Capacitar e involucrar a la facultad en el uso de los recursos electrónicos para que incorporen la literacia de la información de manera tal que impacte el currículo educativo.</p>
<p>Educar y desarrollar las destrezas de información necesarias en la comunidad académica para el uso eficiente de recursos electrónicos tanto en el campus como a distancia.</p>
<p>Solicitar los recursos económicos necesarios para mantener actualizada la tecnología que se requiere.</p>

